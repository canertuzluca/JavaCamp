public class main {
    public static void main (String[] args){

        //For
        for (int i =1;i<10;i++){
            System.out.println(i);
        }

        System.out.println("The Loop Is Over");

        //While
        int j = 1;
        while (j<10){

            System.out.println(j);
            j++;

        }
        System.out.println("The Loop Is Over");

        //Do While
        int k = 1;
        do {
            System.out.println(k);
            k++;
        }while (k<10);

        System.out.println("The Loop Is Over");
    }
}
